import database from "infra/database.js";
import email from "infra/email.js";
import { NotFoundError } from "infra/errors";
import webserver from "infra/webserver.js";

const EXPIRATION_IN_MILLISECONDS = 60 * 15 * 1000 // 15 minutes

async function findOneValidById(tokenId) {
  const activationTokenObject = await runSelectQuery(tokenId);

  return activationTokenObject;

  async function runSelectQuery(tokenId) {
    const results = await database.query({
      text: `
        SELECT
          *
        FROM
          user_activation_tokens
        WHERE
          id = $1
          AND expires_at > NOW()
          AND used_at IS NULL
        LIMIT
        1;
      `,
      values: [tokenId]
    });

    if (results.rowCont === 0) {
      throw new NotFoundError({
        message: "O token de ativação utilizado não foi encontrado no sistema ou expirou.",
        action: "Faça um novo cadastro."
      });
    }
    return results.rows[0];
  }
}

async function create(userId) {
  const expiresAt = new Date(Date.now() + EXPIRATION_IN_MILLISECONDS)

  const newToken = await runInsertQuey(userId, expiresAt);
  return newToken;

  async function runInsertQuey(userId, expiresAt) {
    const results = await database.query({
      text: `
        INSERT INTO
          user_activation_tokens (user_id, expires_at)
        VALUES
          ($1, $2)
        RETURNING
          *
        ;`,
      values: [userId, expiresAt]
    })

    return results.rows[0];
  }
}

async function sendEmailToUser(user, activationToken) {
  await email.send({
    from: "Vini Black <contato@viniblack.com.br>",
    to: user.email,
    subject: "Ative seu cadastro",
    text: `${user.username}, click no link abaixo para ativar seu cadastro

${webserver.origin}/cadastro/ativar/${activationToken.id}

Atenciosamente,
Vini Black.
  `,
  })
}

const activation = {
  create,
  findOneValidById,
  sendEmailToUser,
}

export default activation